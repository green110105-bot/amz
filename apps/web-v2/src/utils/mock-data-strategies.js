function isoDate(daysAgo) {
  const d = new Date('2026-05-14T14:00:00');
  d.setDate(d.getDate() - daysAgo);
  return d.toISOString();
}

function daysAgo(d) {
  return Math.floor((new Date('2026-05-14') - new Date(d)) / 86400000);
}

function pick(arr) { return arr[Math.floor(Math.random() * arr.length)]; }
function pickInt(min, max) { return Math.floor(min + Math.random() * (max - min)); }
function rand(min, max) { return min + Math.random() * (max - min); }

// 14 天触发 sparkline
function makeTriggerHistory(avg) {
  return Array.from({ length: 14 }, () => Math.max(0, Math.round(avg + (Math.random() - 0.5) * avg * 0.8)));
}


// 顶部 KPI
export function strategiesKpi() {
  return {
    total: strategies.length,
    enabled: strategies.filter((s) => s.enabled).length,
    paused: strategies.filter((s) => !s.enabled).length,
    totalTriggered: strategies.reduce((s, r) => s + r.triggerCount, 0),
    avgSuccessRate: (() => {
      const valid = strategies.filter((s) => s.successRate !== null);
      return valid.length ? valid.reduce((sum, r) => sum + r.successRate, 0) / valid.length : 0;
    })(),
    crossModuleCount: strategies.filter((s) => s.crossModule).length,
  };
}
